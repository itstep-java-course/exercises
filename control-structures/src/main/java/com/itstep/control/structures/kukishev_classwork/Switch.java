package com.itstep.control.structures.kukishev_classwork;

public class Switch {
    public static void main(String[] args) {
        String word = "Bad";

        switch (word) {
            case "Hello":
                System.out.println("Привет");
                break;
            case "Good":
                System.out.println("Хорошо");
                break;
            case "Bad":
                System.out.println("Плохо");
                break;
            default:
                System.out.println("Кравозяблы");
        }



    }





}
