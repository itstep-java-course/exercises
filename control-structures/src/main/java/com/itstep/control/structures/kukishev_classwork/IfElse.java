package com.itstep.control.structures.kukishev_classwork;

public class IfElse {

    public static void main(String[] args) {

        int x = 655;

        if (x == 6) {
            System.out.println("6");
        } else if (x == 3) {
            System.out.println("3");
        } else if (x <= 5) {
            System.out.println("5");
        } else {
            System.out.println("0");
        }


    }


}
