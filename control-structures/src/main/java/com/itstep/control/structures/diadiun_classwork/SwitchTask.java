package com.itstep.control.structures.diadiun_classwork;



public class SwitchTask {
    public static void main(String[] args) {
        String word="Hello";

    switch (word){
        case "Hello":
            System.out.println("Привет");
            break;
        case "Bad":
            System.out.println("Плохой");
            break;
        case "Good":
            System.out.println("Хорошо");
            break;
        default:
            System.out.println("Что то не понятное....");
    }




    }

}
