package com.itstep.control.structures.kukishev_hometask;


public class Switch {

    public static void main(String[] args) {
        String word = "Synchrophasotron";

        switch (word) {

            case "Synchrophasotron":
                System.out.println("Синхрофазотрон");
                break;

            case "Space":
                System.out.println("Космос");
                break;

            case "Interstellar":
                System.out.println("Межзвёздный");
                break;

            default:
                System.out.println("Загугли");




        }
    }


}

