package com.itstep.control.structures.huk_classwork;

public class IfElseTask {
    public static void main(String[] args) {
        int x = 7;

        if (x == 6) {
            System.out.println("x = 6");
        } else if (x == 3) {
            System.out.println("x = 3");
        } else if (x <= 5) {
            System.out.println("x = 5");
        } else {
            System.out.println("0");
        }
    }
}





