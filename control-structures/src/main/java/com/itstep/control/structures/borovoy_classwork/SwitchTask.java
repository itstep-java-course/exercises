package com.itstep.control.structures.borovoy_classwork;

public class SwitchTask {
    public static void main(String[] args) {
        String translator = "Hello";


        switch (translator) {

            case "Hello":
                System.out.println("Привет");
                break;
            case "Bad":
                System.out.println("Плохо");
                break;
            case "Good":
                System.out.println("Хорошо");
                break;
        }
    }


}
