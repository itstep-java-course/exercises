package com.itstep.string.array.kukishev_homework;

public class MultiArrayTask {
    public static void main(String[] args) {

        int [][] mass_complex = new int [3][3];
        mass_complex[0] = new int [] {4, 5, 5};
        mass_complex[1] = new int [] {3, 12, 7};
        mass_complex [2] = new int [] {5, 6, 10};

        for (int i = 0; i < mass_complex.length; i++) {

            for (int i1 = 0; i1 < mass_complex.length; i1++) {





                int element = mass_complex [i][i1];
                System.out.println(element);

            }

        }




    }


}
